'''
Дано одновимірний масив дійсних чисел.
Знайти суму елементів, які розташовані до першого від’ємного елемента.
'''
# позначення
"""
Suma - сума
а - елемент float
s - список 
"""
# введення
Suma = 0
a = int(input('введіть'))
s = [float(input()) for i in range(a)]
# перевірка умови
for i in range(len(s)):
    if s[i] > 0 :
        Suma += s[i]
    else :
        break
# виведення
print(Suma)